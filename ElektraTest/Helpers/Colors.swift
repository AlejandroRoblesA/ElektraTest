import UIKit

extension UIViewController{
    func elektraGray() -> UIColor {
        return UIColor(red: 247/255, green: 247/255, blue: 247/255, alpha: 1)
    }
    
    func elektraBlue() -> UIColor {
        return UIColor(red: 80/255, green: 110/255, blue: 130/255, alpha: 1)
    }
}
